class Math:
    ''' In this math class i am implementing few functions.
        those are isEven,isOdd and power.
    '''

    def isEven(num):
        if num%2==0:
            return True
        return False

    def isOdd(val):
        if val%2!=0:
            return True
        return False

    def power(bval,pval):
        return bval**pval






    
        
    
